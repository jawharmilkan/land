package model;

/**
 *
 * @author jawhar
 */
public class CommitteeInfo 
{
    private String id, name, address, phoneNumber;

    public CommitteeInfo(String id, String name, String address, String phoneNumber) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.phoneNumber = phoneNumber;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    @Override
    public String toString() {
        return "CommitteeInfo{" + "id=" + id + ", name=" + name + ", address=" + address + ", phoneNumber=" + phoneNumber + '}';
    }
    
    
}
